
public class ascii {
    public static void main(String[] args)   
    {  
        for(int i = 0; i <= 255; i++)  
        {  
            System.out.println(" The ASCII value of " + (char)i + "  =  " + i);  
        }  
        for(int i = 65; i <= 90; i++)  
        {  
            System.out.println(" The ASCII value of " + (char)i + "  =  " + i);  
        }  
        
    }      
}
